export type IconName = keyof typeof ICONS;

export class Icon extends Component<HTMLSpanElement> {
  constructor(name: IconName, options: IconOptions = {}) {
    super('span', {
      className: 'icon',
      ...options,
    });

    const { size = 'md', color, label } = options;

    this.addClass(`icon--${size}`);

    if (color) {
      this.addClass(`icon--${color}`);
    }

    // Set the SVG content
    this.element.innerHTML = ICONS[name];

    // Accessibility
    if (label) {
      this.setAttribute('role', 'img');
      this.setAttribute('aria-label', label);
    } else {
      this.setAttribute('aria-hidden', 'true');
    }
  }

  /**
   * Change the icon
   */
  setIcon(name: IconName): this {
    this.element.innerHTML = ICONS[name];
    return this;
  }
}

/**
 * IconButton - A button that contains only an icon
 */
export interface IconButtonOptions extends IconOptions {
  onClick?: (event: MouseEvent) => void;
}

export class IconButton extends Component<HTMLButtonElement> {
  private icon: Icon;

  constructor(name: IconName, options: IconButtonOptions = {}) {
    super('button', {
      className: 'icon-button',
      attributes: {
        type: 'button',
        'aria-label': options.label ?? name,
      },
    });

    this.icon = new Icon(name, {
      size: options.size,
      color: options.color,
    });

    this.element.appendChild(this.icon.getElement());

    if (options.onClick) {
      this.on('click', options.onClick);
    }
  }

  /**
   * Change the icon
   */
  setIcon(name: IconName): this {
    this.icon.setIcon(name);
    return this;
  }

  /**
   * Set disabled state
   */
  setDisabled(disabled: boolean): this {
    this.element.disabled = disabled;
    return this;
  }
}
